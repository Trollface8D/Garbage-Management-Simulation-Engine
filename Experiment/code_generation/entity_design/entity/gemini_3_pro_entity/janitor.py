import random
from typing import TYPE_CHECKING, Optional
from entity_object_template import entity_object

if TYPE_CHECKING:
    from environment_template import SimulationEnvironment

class Janitor(entity_object):
    """
    Janitor (Housekeeper/Maid) entity for the waste management simulation.
    
    This is an ACTIVE entity that perceives trash levels and time of day,
    decides when to collect, sort, weigh, and dump waste, and acts upon 
    the environment (specifically the waste holding cages).
    """
    
    def __init__(self, entity_object_id: str):
        super().__init__(entity_object_id)
        self.state = "Idle"
        self.collected_waste_volume = 0.0
        self.recorded_weight = 0.0
        self.current_time = "morning" # Can be 'morning', 'afternoon', '4pm', '5pm', etc.
        self.mood = random.choice(["good", "rushed", "indifferent"])
        self.action_intent = None

    # ==================== ACTIVE TRAIT METHODS ====================
    
    def perceive(self, env: Optional['SimulationEnvironment'] = None):
        """
        Gathers information from the environment, such as the time of day 
        and whether there is waste to be collected from the building.
        """
        if env:
            self.current_time = env.get_time_of_day()
            # Janitor checks if there is waste generated by students/staff
            self.waste_to_collect = env.check_building_waste_level(self.entity_object_id)
        else:
            # Fallback for standalone testing
            self.waste_to_collect = True 

    def decide_action(self) -> Optional[str]:
        """
        Decides the next action based on the time of day and accumulated waste.
        """
        if self.state == "Idle" and self.waste_to_collect:
            self.action_intent = "collect_and_sort"
            return self.action_intent
            
        elif self.state == "Holding_Waste":
            self.action_intent = "weigh_waste"
            return self.action_intent
            
        elif self.state == "Ready_To_Dump":
            # Logic derived from: "แต่แม่บ้าน เลิกงานกี่โมง 5 โมง เขาจะถือขยะลงมาทิ้ง ตอน 5 โมง"
            # Logic derived from: "อย่างสมมติ รถรอบเนี่ยมาเก็บตอน 3 โมง แม่บ้านที่อยู่ในอาคารเอามาทิ้งตอน 4 โมง..."
            # Janitors frequently dump at 4 PM or 5 PM (end of shift), or in the morning
            if self.current_time in ["morning", "4pm", "5pm"]:
                self.action_intent = "dump_waste"
                return self.action_intent
                
        self.action_intent = "idle"
        return self.action_intent

    def act(self, env: Optional['SimulationEnvironment'] = None):
        """
        Executes the decided action, interacting with the environment's waste holding points.
        """
        if self.action_intent == "collect_and_sort":
            self._perform_collect_and_sort()
            
        elif self.action_intent == "weigh_waste":
            self._perform_weighing()
            
        elif self.action_intent == "dump_waste":
            self._perform_dumping(env)
            
        elif self.action_intent == "idle":
            self.state = "Idle"

    # ==================== INTERNAL BEHAVIOR LOGIC ====================

    def _perform_collect_and_sort(self):
        """
        Simulates collecting building waste and doing a basic separation.
        """
        # Logic derived from: "แม่บ้านที่จะคอยดูแล ก็คือจะมาคอยเก็บ ช่วยคัดแยกในระดับนึง จากการที่นักศึกษา หรือบุคลากรทิ้ง"
        # Housekeepers sort partially triggered by students/staff discarding waste.
        self.collected_waste_volume = random.uniform(5.0, 15.0) 
        self.state = "Holding_Waste"
        # In a more complex model, we would split the waste object into 'sortable' and 'unsortable' here.

    def _perform_weighing(self):
        """
        Simulates the weighing process, capturing the data inaccuracy issues.
        """
        # Logic derived from: "คือเราอะ ต้องการข้อมูล ที่มันเป็น ข้อมูลจริง จริงๆ นะ ไม่ใช่ข้อมูลที่ แม่บ้านเขา แล้วแต่อารมณ์ อยากจะเขียนน่ะ"
        # Logic derived from: "พี่อะ แซวตลอดอะ ว่า วันนี้ชอบเลขอะไร ยกโชก (ยกโชค/เสี่ยงโชค) ขยะลงมาทิ้งเนี่ย ชอบเลขอะไร..."
        # Maids guessing numbers or relying on mood causes extreme data inaccuracy.
        
        if self.mood == "rushed" or random.random() > 0.5:
            # Guessing a number like a lottery instead of actual weighing
            self.recorded_weight = float(random.choice([9, 18, 27, 99])) 
        else:
            # Actual approximate weight
            self.recorded_weight = self.collected_waste_volume * 1.2 
            
        self.state = "Ready_To_Dump"

    def _perform_dumping(self, env: Optional['SimulationEnvironment']):
        """
        Simulates moving the waste to the holding cage, frequently causing overflow.
        """
        # Logic derived from: "เขาก็จะเก็บรวบรวมแต่ละถัง ชั่งน้ำหนัก แล้วก็เอาไปไว้ที่กรงพักขยะ"
        # Logic derived from: "แต่มันก็ ยังไม่ได้ ตอบโจทย์ หรือมันก็ ไม่ง่าย ต่อแม่บ้าน เอง เหมือนกัน อืม ซึ่งมันก็ เล็กเกินไป"
        
        if env:
            target_cage = env.get_nearest_waste_cage(self.entity_object_id)
            
            # Logic derived from: "เพราะว่าแม่บ้านเนี่ยจะเก็บขยะรอบเช้าเนี่ย มาอัด"
            # Logic derived from: "สรุปขยะมันก็จะถูกอัด อัดๆๆ มาจนล้นตลอด"
            if self.current_time == "morning":
                env.interact_with_object(target_cage, "compress_and_add_waste", self.collected_waste_volume)
            else:
                env.interact_with_object(target_cage, "add_waste", self.collected_waste_volume)
                
            # Logic derived from: "แต่มันก็ทำให้รอบการเก็บ มันพลาดกัน ทำให้เกิดปัญหา... มันก็เป็นขยะที่ตกค้าง"
            if self.current_time in ["4pm", "5pm"]:
                env.log_event(f"Residue waste created by Janitor {self.entity_object_id} missing the 3 PM truck.")
                
        self.collected_waste_volume = 0.0
        self.recorded_weight = 0.0
        self.state = "Idle"